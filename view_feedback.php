            <th>Rating</th>
            <th>Submission Date</th>
        </tr>
        <?php
        if ($result->num_rows > 0) {
            while($row = $result->fetch_assoc()) {
                echo "<tr>
                        <td>" . $row["id"] . "</td>
                        <td>" . $row["name"] . "</td>
                        <td>" . $row["email"] . "</td>
                        <td>" . $row["feedback"] . "</td>
                        <td>" . $row["rating"] . "</td>
                        <td>" . $row["submission_date"] . "</td>
                      </tr>";
            }
